public class VariablesDemo {
    public static void main(String[] args) {
        boolean female = true;
        char c = 'A';
        byte b = 10;
        short s = 10001;
        int i = 200;
        long l = 20000;
        float radius = 30.2f;
        double pi = 3.14d;
        System.out.println("boolean value is :" + female);
        System.out.println("character value is :" + c);
        System.out.println("byte value is :" + b);
        System.out.println("short value is :" + s);
        System.out.println("integer value is :" + i);
        System.out.println("long value is :" + l);
        System.out.println("float value is :" + radius);
        System.out.println("double value is :" + pi);
        

        //constant and static variable
        final String collegeName="Pillai hoc";
        System.out.println("college name is:"+collegeName );
        /* 
        collegeName="MGM college";
         System.out.println("college name is:"+collegeName );*/
   }
}
